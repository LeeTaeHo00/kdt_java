package ifTest;
// if~else
public class IfTest02 { // 클래스 시작

	public static void main(String[] args) { // 메인 메소드 시작
		System.out.println("출력 시작"); // 출력
		
		if(false) { // if문 영역
			System.out.println("조건식이 true 입니다."); // 출력
		}else {	// else 영역
			System.out.println("조건식이 false 입니다."); // 출력
		}
		
		System.out.println("출력 끝"); // 출력

	} // 메인 메소드 끝

} // 클래스 끝
